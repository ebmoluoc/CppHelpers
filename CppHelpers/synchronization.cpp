#include "pch.h"

///////////////////////////////////////////////////////////////////////////////////////////////////

namespace hlp
{

	bool MutexExists(LPCWSTR name)
	{
		auto mutex{ OpenMutexW(SYNCHRONIZE, FALSE, name) };
		auto exists{ mutex != nullptr };
		if (exists)
		{
			CloseHandle(mutex);
		}

		return exists;
	}

	///////////////////////////////////////////////////////////////////////////////////////////////

}
